def addition(number1, number2):
    result = number1 + number2
    return result

def subtraction(number1, number2):
    result = number1 - number2
    return result


def multiplication(number1, number2):
    result = number1 * number2
    return result

def division(number1, number2):
    result = number1 / number2
    return result

def increment(number):
    result = number + 1
    return result

def decrement(number):
    result = number - 1
    return result

result = addition(10,12)
print(result)

result = subtraction(58,34)
print(result)

result = multiplication(6,7)
print(result)

result = division(144,12)
print(result)
result = increment(12)
print(result)

result = decrement(34)
print(result)



